// Declare a variable for the rectangle length
const length = 10;

// Declare a variable for the rectangle width
const width = 5;

// Calculate the area of the rectangle using the formula length * width
const area = length * width;

// Round to 2 decimal places
const roundedArea = area.toFixed(2);

// Output the result to the console
console.log("Length:", length);
console.log("Width:", width);
console.log("Area of the rectangle:", roundedArea);