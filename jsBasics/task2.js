// Declare two variables with names
const firstName = "Olena";
const secondName = "Alex";

// String concatenation
const greetingConcat = "Hello, " + firstName + " and " + secondName + "!";
console.log("Concatenation:", greetingConcat);

// Template literal
const greetingTemplate = `Hello, ${firstName} and ${secondName}!`;
console.log("Template literal:", greetingTemplate);